package Operadores.aritmetivos;

public class ArtmeticoAtribuicao {
    public static void main(String[] args) {

        // Atribuição =
        // Soma +
        int numeroSoma = 21 + 52;

        // Subtração -
        int numeroSubtracao = 52 - 12;

        // Divisão /
        int numeroDivisao = 45 / 3;

        // Multiplicação *
        int numeroMultiplicacao = 65 * 3;

        // Modulo
        int numeroModulo = 4 % 2;

        // Incremento ++
        int numeroIncremento = 1;
        numeroIncremento++;

        // Decremento --
        int numeroDecremento = 1;
        numeroDecremento--;

    }
}
